//likebutton1
const likeBtn1 = document.querySelector(".like__btn1");
let like_Icon1 = document.querySelector("#like_icon1"),
  like_count1 = document.querySelector("#like_count1");
let like_clicked_1 = false;
likeBtn1.addEventListener("click", () => {
  if (!like_clicked_1) {
    like_clicked_1 = true;
    like_Icon1.innerHTML = `<i class="fas fa-thumbs-up"></i>`;
    like_count1.textContent++;
  } else {
    like_clicked_1 = false;
    like_Icon1.innerHTML = `<i class="far fa-thumbs-up"></i>`;
    like_count1.textContent++;
  }
});
